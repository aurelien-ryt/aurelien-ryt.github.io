let currentLang = 'fr';

function applyLang(lang) {
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[lang][key] !== undefined) el.innerHTML = t[lang][key];
  });
  document.getElementById('langBtn').textContent = lang === 'fr' ? 'EN' : 'FR';
  currentLang = lang;
}

document.getElementById('langBtn').addEventListener('click', () => {
  applyLang(currentLang === 'fr' ? 'en' : 'fr');
});


/* ═══════════════════════════════════════════
   SCROLL REVEAL  (IntersectionObserver)
═══════════════════════════════════════════ */

// Give skill badges staggered delays before observing
document.querySelectorAll('.skill-group').forEach(group => {
  group.querySelectorAll('.badge').forEach((badge, i) => {
    badge.classList.add('reveal');
    badge.dataset.stagger = i * 0.07;
  });
});

const revealObserver = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      const delay = parseFloat(entry.target.dataset.stagger || 0);
      entry.target.style.transitionDelay = `${delay}s`;
      entry.target.classList.add('visible');
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.12, rootMargin: '0px 0px -50px 0px' });

document.querySelectorAll('.reveal').forEach(el => revealObserver.observe(el));


